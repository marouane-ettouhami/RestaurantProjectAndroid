package com.example.miniprojet.models;

public class Category {
    String id;
    String name;

    public Category( String name) {

        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }




}
